package week1learningJava;

public class ArithmeticOperator_2 {

	public static void main(String[] args) {
		int a=20;
		int b=10;
System.out.println("The addtion of two number is : " + (a+b));
System.out.println("The subtraction of two number is : " + (a-b));
System.out.println("The Multiplication of two number is : " + (a*b));
System.out.println("The Division-quotient two number is : " + (a/b));
System.out.println("The Division-reminder two number is : " + (a%b));
System.out.println("A incremental by 1 is : " + (a++));
System.out.println("B decremental by 1 is : " + (b--));
	}

}
